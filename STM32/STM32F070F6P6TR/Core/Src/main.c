#define max(X, Y) (((X) < (Y)) ? (Y) : (X))
#define min(X, Y) (((X) > (Y)) ? (Y) : (X))

#define UART2_rxBuffer_size 16
uint8_t UART2_rxBuffer[UART2_rxBuffer_size];

#define numMotors 5

uint8_t motorInFocus = 0;
float desiredMotorAngles[numMotors];
float currentMotorAngles[numMotors];
float currentMotorVelocities[numMotors];

// ORIGINAL 180 - 1050
#define motorLoBase 540
#define motorHiBase 3150
const float motorRangeBase = motorHiBase - motorLoBase;

float motorLo[numMotors] = {
		motorLoBase + motorRangeBase*27/255,
		motorLoBase + motorRangeBase*29/255,
		motorLoBase + motorRangeBase*3/255,
		motorLoBase,
		motorLoBase + motorRangeBase*74/255
};

float motorHi[numMotors] = {
		motorHiBase,
		motorLoBase + motorRangeBase*195/255,
		motorLoBase + motorRangeBase*245/255,
		motorHiBase,
		motorLoBase + motorRangeBase*160/255
};

float motorMaxVel[numMotors] = { 10, 8, 5, 20, 50};
float motorVelIncr[numMotors] = { 0.03, 0.03, 0.03, 0.06, 0.50};

float velIncrBase = 0.03, velBase = 0.1;



void updateMotorAngles() {
	TIM3->CCR1 = currentMotorAngles[0];
	TIM3->CCR2 = currentMotorAngles[1];
	TIM3->CCR4 = currentMotorAngles[2];
	TIM14->CCR1 = currentMotorAngles[3];
	TIM1->CCR2 = currentMotorAngles[4];
}

void moveMotorAngles() {
	for(int i=0; i<numMotors; i++) {
		currentMotorAngles[i] += currentMotorVelocities[i];
	}
}


float newVelocity(int32_t motorIdx) {

	float a = 300, b = motorMaxVel[motorIdx];
	float dist = desiredMotorAngles[motorIdx] - currentMotorAngles[motorIdx];

	// Close enough, snap to angle
	if (abs(dist) < 1) {
		currentMotorAngles[motorIdx] = desiredMotorAngles[motorIdx];
		return 0;
	}


	if (dist > 0) {

		return max(velBase, min( currentMotorVelocities[motorIdx] + motorVelIncr[motorIdx], (min(b, b*dist/a)) ) );

	} else if (dist < 0) {
		return min(-velBase, max( currentMotorVelocities[motorIdx] - motorVelIncr[motorIdx], - (min(b, b*(-dist)/a)) ) );

	}
	return 0;

}


void approachMotorAngles() {

	for(int i=0; i<numMotors; i++) {
		currentMotorVelocities[i] = newVelocity(i);
	}
	moveMotorAngles();
	updateMotorAngles();
}

